fun multiplicar(num1: Int , num2: Int) -> Int {
    return num1 * num2
}

fun division(num1: Int , num2: Int) -> Int {
    return num1 / num2
}

fun resta(num1: Int , num2: Int) -> Int {
    return num1 - num2
}

fun suma(num1: Int , num2: Int) -> Int {
    return num1 + num2
}

